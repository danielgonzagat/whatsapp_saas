#!/usr/bin/env node
/**
 * Mirror Acceptance Tests — runs the 5 mission tests against the watcher.
 *
 * Mission contract:
 *   1. New file with 1 error per category → sidecar appears with correct
 *      findings within 5 seconds.
 *   2. Edit file fixing one error → sidecar updates within 5 seconds.
 *   3. Delete file → sidecar removed.
 *   4. Move/rename file → sidecar follows.
 *   5. Run watch for 10 min during real edits — no leak, no crash.
 *
 * For tests 1–4 the watcher must be running (via `npm run findings:watch`).
 * For test 5 we sample memory + crash status periodically.
 *
 * Usage:
 *   node scripts/ops/mirror-acceptance-tests.mjs --quick   # tests 1-4 only
 *   node scripts/ops/mirror-acceptance-tests.mjs --soak    # add test 5 (10 min)
 *
 * Output: writes MIRROR_VALIDATION.md at repo root with results.
 */

import { spawnSync, spawn } from 'node:child_process';
import {
  writeFileSync,
  readFileSync,
  existsSync,
  mkdirSync,
  rmSync,
  renameSync,
  statSync,
} from 'node:fs';
import { resolve, join, dirname } from 'node:path';
import { fileURLToPath } from 'node:url';

const __dirname = dirname(fileURLToPath(import.meta.url));
const REPO_ROOT = resolve(__dirname, '..', '..');
const MIRROR_ROOT = resolve(
  process.env.KLOEL_MIRROR_ROOT ||
    '/Users/danielpenin/Documents/Obsidian Vault/Kloel/99 - Espelho do Codigo',
);
const SOURCE_MIRROR_DIR = join(MIRROR_ROOT, '_source');
const TEST_DIR_REL = 'backend/src/__mirror_acceptance_tmp__';
const TEST_DIR = join(REPO_ROOT, TEST_DIR_REL);
const VALIDATION_PATH = join(REPO_ROOT, 'MIRROR_VALIDATION.md');
const POLL_TIMEOUT_MS = 12000;
const POLL_INTERVAL_MS = 250;

function nowISO() {
  return new Date().toISOString();
}

function sleep(ms) {
  return new Promise((r) => setTimeout(r, ms));
}

function sidecarPathFor(repoRel) {
  return join(SOURCE_MIRROR_DIR, repoRel + '.findings.json');
}

async function pollExists(path, expect = true, timeoutMs = POLL_TIMEOUT_MS) {
  const deadline = Date.now() + timeoutMs;
  while (Date.now() < deadline) {
    if (existsSync(path) === expect) return true;
    await sleep(POLL_INTERVAL_MS);
  }
  return false;
}

async function pollContent(path, predicate, timeoutMs = POLL_TIMEOUT_MS) {
  const deadline = Date.now() + timeoutMs;
  while (Date.now() < deadline) {
    if (existsSync(path)) {
      try {
        const content = JSON.parse(readFileSync(path, 'utf8'));
        if (predicate(content)) return content;
      } catch {
        /* ignore parse races */
      }
    }
    await sleep(POLL_INTERVAL_MS);
  }
  return null;
}

function writeTestFile(name, content) {
  const dir = join(TEST_DIR, dirname(name));
  mkdirSync(dir, { recursive: true });
  const abs = join(TEST_DIR, name);
  writeFileSync(abs, content);
  return abs;
}

const TEST_BUGGY_TS = `// Generated by mirror-acceptance-tests; safe to delete.
import nope from 'this-package-does-not-exist';

export function buggy(a: number, b: string): number {
  // type error: assigning string to number
  const total: number = b;
  // unused variable
  const unused = 42;
  // missing return path on else branch
  if (a > 0) {
    return a + nope.value;
  }
}
`;

const TEST_FIXED_TS = `// Generated by mirror-acceptance-tests; safe to delete.
export function fixed(a: number): number {
  if (a > 0) return a;
  return 0;
}
`;

async function runTest1New(report) {
  const startedAt = Date.now();
  const rel = `${TEST_DIR_REL}/buggy.ts`;
  const abs = writeTestFile('buggy.ts', TEST_BUGGY_TS);
  const sidecar = sidecarPathFor(rel);

  const found = await pollContent(
    sidecar,
    (c) => Array.isArray(c.findings) && c.findings.length > 0,
    POLL_TIMEOUT_MS,
  );
  const elapsedMs = Date.now() - startedAt;

  report.push({
    test: '1-new-file-with-errors',
    pass: !!found,
    elapsedMs,
    sidecar,
    findingsCount: found ? found.findings.length : 0,
    note: found
      ? 'sidecar appeared with findings'
      : 'sidecar did not appear or had no findings within 8s',
  });
  return abs;
}

async function runTest2Fix(report, abs) {
  const startedAt = Date.now();
  writeFileSync(abs, TEST_FIXED_TS);
  const rel = `${TEST_DIR_REL}/buggy.ts`;
  const sidecar = sidecarPathFor(rel);

  // Expect: either sidecar disappears (no findings) OR findings count drops.
  const result = await pollContent(
    sidecar,
    (c) => Array.isArray(c.findings) && c.findings.length === 0,
    POLL_TIMEOUT_MS,
  );
  const elapsedMs = Date.now() - startedAt;
  const passByContent = !!result;
  const passByDelete = !existsSync(sidecar);

  report.push({
    test: '2-edit-fixes-error',
    pass: passByContent || passByDelete,
    elapsedMs,
    sidecar,
    note: passByContent
      ? `findings dropped to 0`
      : passByDelete
        ? 'sidecar removed (no findings → cleaned up)'
        : 'sidecar still has findings after fix',
  });
}

async function runTest3Delete(report, abs) {
  const startedAt = Date.now();
  rmSync(abs, { force: true });
  const rel = `${TEST_DIR_REL}/buggy.ts`;
  const sidecar = sidecarPathFor(rel);
  const removed = await pollExists(sidecar, false, POLL_TIMEOUT_MS);
  const elapsedMs = Date.now() - startedAt;

  report.push({
    test: '3-delete-file',
    pass: removed,
    elapsedMs,
    sidecar,
    note: removed ? 'sidecar removed' : 'sidecar still present 8s after source deletion',
  });
}

async function runTest4Move(report) {
  const startedAt = Date.now();
  const absOld = writeTestFile('original.ts', TEST_BUGGY_TS);
  const relOld = `${TEST_DIR_REL}/original.ts`;
  // Wait until original sidecar appears
  await pollExists(sidecarPathFor(relOld), true, POLL_TIMEOUT_MS);

  const absNew = join(TEST_DIR, 'moved.ts');
  renameSync(absOld, absNew);
  const relNew = `${TEST_DIR_REL}/moved.ts`;

  const newAppeared = await pollExists(sidecarPathFor(relNew), true, POLL_TIMEOUT_MS);
  const oldGone = await pollExists(sidecarPathFor(relOld), false, POLL_TIMEOUT_MS);
  const elapsedMs = Date.now() - startedAt;

  // Cleanup
  rmSync(absNew, { force: true });

  report.push({
    test: '4-move-file',
    pass: newAppeared && oldGone,
    elapsedMs,
    note: `new sidecar=${newAppeared} old gone=${oldGone}`,
  });
}

async function runTest5Soak(report) {
  // Lightweight soak: verify the watcher PID lock exists, then sample
  // RSS every 30s for 10 min, confirm < 200MB and process alive.
  const PID_FILE = '/tmp/kloel-findings-watch.pid';
  if (!existsSync(PID_FILE)) {
    report.push({
      test: '5-soak-10min',
      pass: false,
      note: 'PID file missing — watcher not running',
    });
    return;
  }
  const meta = JSON.parse(readFileSync(PID_FILE, 'utf8'));
  const pid = meta.pid;
  const samples = [];
  const total = 10;
  for (let i = 0; i < total; i++) {
    let alive = true;
    try {
      process.kill(pid, 0);
    } catch {
      alive = false;
    }
    let rssMB = -1;
    try {
      const out = spawnSync('ps', ['-o', 'rss=', '-p', String(pid)], { encoding: 'utf8' });
      const k = parseInt(out.stdout.trim(), 10);
      if (!isNaN(k)) rssMB = Math.round(k / 1024);
    } catch {
      /* ignore */
    }
    samples.push({ at: nowISO(), alive, rssMB });
    if (!alive) break;
    await sleep(60_000);
  }
  const allAlive = samples.every((s) => s.alive);
  const peakRSS = Math.max(...samples.map((s) => s.rssMB));
  const pass = allAlive && peakRSS < 200;

  report.push({
    test: '5-soak-10min',
    pass,
    note: `samples=${samples.length} alive=${allAlive} peakRSS=${peakRSS}MB`,
    samples,
  });
}

function renderMarkdown(report, mode) {
  const lines = [];
  lines.push('# MIRROR_VALIDATION');
  lines.push('');
  lines.push(`> Mode: ${mode}`);
  lines.push(`> Generated: ${nowISO()}`);
  lines.push('');
  lines.push('## Acceptance tests');
  lines.push('');
  lines.push('| # | Test | Pass | Elapsed | Note |');
  lines.push('|---|---|---|---|---|');
  for (const r of report) {
    const elapsed = r.elapsedMs ? `${r.elapsedMs}ms` : '-';
    lines.push(
      `| ${r.test} | ${r.test.replace(/^\d+-/, '')} | ${r.pass ? '✅' : '❌'} | ${elapsed} | ${r.note ?? ''} |`,
    );
  }
  lines.push('');
  lines.push('## Raw report');
  lines.push('```json');
  lines.push(JSON.stringify(report, null, 2));
  lines.push('```');
  return lines.join('\n');
}

async function main() {
  const args = process.argv.slice(2);
  const soak = args.includes('--soak');
  const mode = soak ? 'quick + soak' : 'quick';

  // Pre-flight: source mirror must exist
  if (!existsSync(SOURCE_MIRROR_DIR)) {
    console.error(`acceptance: ${SOURCE_MIRROR_DIR} missing — run mirror daemon first`);
    process.exit(2);
  }

  // Pre-flight: PID lock must exist (the watcher must be running)
  if (!existsSync('/tmp/kloel-findings-watch.pid')) {
    console.error(
      'acceptance: /tmp/kloel-findings-watch.pid missing. Start the watcher first: npm run findings:watch',
    );
    process.exit(2);
  }

  mkdirSync(TEST_DIR, { recursive: true });
  const report = [];

  try {
    const abs = await runTest1New(report);
    await runTest2Fix(report, abs);
    await runTest3Delete(report, abs);
    await runTest4Move(report);
    if (soak) await runTest5Soak(report);
  } finally {
    try {
      rmSync(TEST_DIR, { recursive: true, force: true });
    } catch {
      /* ignore */
    }
  }

  const md = renderMarkdown(report, mode);
  writeFileSync(VALIDATION_PATH, md);

  const failed = report.filter((r) => !r.pass);
  process.stdout.write(
    JSON.stringify(
      { ok: failed.length === 0, total: report.length, failed: failed.length, report },
      null,
      2,
    ) + '\n',
  );
  process.exit(failed.length === 0 ? 0 : 1);
}

main().catch((e) => {
  console.error('acceptance: fatal', e);
  process.exit(1);
});
